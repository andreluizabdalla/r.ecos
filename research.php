                        This paper presents R.ECOS, an approach from the Software Ecosystems (SECO) perspective to recommend Educational Resources. 
                        It defines a platform that allows services integration and other Ecosystems services, 
                        with the aim of facilitating the reuse and sharing of solutions in Recommender Systems. 
                        The development of the proposal advances on studies of our Research Group, especially in the educational area. 
                        An initial assessment of the proposal was made through an application scenario, 
                        in which the services of R.ECOS were developed to define groups of users. 
                        Additionally, services from third-party were reused. 
                        The obtained results in the application scenario point to the viability of our proposal.

